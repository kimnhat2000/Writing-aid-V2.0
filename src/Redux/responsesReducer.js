export const responsesReducer = (state = responsesDefaultData, action) => {
  switch (action.type) {
    case 'SELECTED_TITLE':
      const clickOnTitle = state.responsesData && {
        ...state,
        responsesData: state.responsesData.map(title =>
          title.id === action.titleId
            ? { ...title, collapse: !title.collapse }
            : { ...title, collapse: true }
        )
      }
      return { ...clickOnTitle, selectedTitleId: action.titleId }
    case 'SELECTED_OPTION':
      const newstate = {
        ...state,
        responsesData: state.responsesData.map(title =>
          title.id === action.selectedTitleId
            ? {
              ...title,
              possibleAnswers: title.possibleAnswers.map(answer =>
                answer.id === action.SelectedOptionId
                  ? { ...answer, selected: !answer.selected }
                  : { ...answer, selected: false, copy: false }
              )
            }
            : title
        )
      }
      return { ...newstate, ...action }
    case 'COPY_OPTION':
      const copystate = {
        ...state,
        responsesData: state.responsesData.map(title =>
          title.id === action.selectedTitleId
            ? {
              ...title,
              possibleAnswers: title.possibleAnswers.map(answer =>
                answer.id === action.SelectedOptionId
                  ? { ...answer, copy: !answer.copy }
                  : { ...answer, selected: false, copy: false }
              )
            }
            : title
        )
      }
      return { ...copystate }
    case 'ADD_TITLE':
      const newTitle = {
        ...state,
        responsesData: [action.newTitle, ...state.responsesData]
      }
      return newTitle

    default:
      return state
  }
}
